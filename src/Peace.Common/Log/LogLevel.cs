        /// <summary>
        /// 提示日志
        /// </summary>
        Info = 2,

        /// <summary>
        /// 警告日志
        /// </summary>
        Warn = 3,

        /// <summary>
        /// 异常日志
        /// </summary>
        Error = 4,

        /// <summary>
        /// 严重错误日志
        /// </summary>
        Fatal = 5
    }
}
